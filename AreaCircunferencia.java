package Fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		
		double raio = 3.14;
		
		final double PI = 3.1415;
		double area =  PI * raio * raio;
		
		System.out.println("A area é " + area + " m2. ");
		
		
		raio = 10;
		area =  PI * raio * raio;
		
		System.out.println("A area é " + area + " m2. ");
	}

}
